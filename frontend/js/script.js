let currentPage = 1;
const hospitalsPerPage = 10;

document.getElementById("districtSelect").addEventListener("change", function () {
    currentPage = 1;
    displayHospitals();
});

function displayHospitals() {
    const selectedDistrict = document.getElementById("districtSelect").value;
    const hospitalListDiv = document.getElementById("hospital-list");

    // Clear previous content
    hospitalListDiv.innerHTML = "";

    if (selectedDistrict && hospitals[selectedDistrict]) {
        const totalHospitals = hospitals[selectedDistrict].length;
        const totalPages = Math.ceil(totalHospitals / hospitalsPerPage);
        const startIndex = (currentPage - 1) * hospitalsPerPage;
        const endIndex = startIndex + hospitalsPerPage;
        const hospitalsToShow = hospitals[selectedDistrict].slice(startIndex, endIndex);

        let table = `<table style="width:100%; border-collapse: collapse; text-align: left;">
            <thead>
                <tr style="background-color: #f2f2f2;">
                    <th style="border: 1px solid #ddd; padding: 16px; font-weight: bold;">Hospital Name</th>
                    <th style="border: 1px solid #ddd; padding: 16px; font-weight: bold;">Type</th>
                    <th style="border: 1px solid #ddd; padding: 16px; font-weight: bold;">Mandal</th>
                    <th style="border: 1px solid #ddd; padding: 16px; font-weight: bold;">Municipality</th>
                    <th style="border: 1px solid #ddd; padding: 16px; font-weight: bold;">Mithra Contacts</th>
                    <th style="border: 1px solid #ddd; padding: 16px; font-weight: bold;">Specialities</th>
                </tr>
            </thead>
            <tbody>`;

        hospitalsToShow.forEach(hospital => {
            table += `<tr>
                <td style="border: 1px solid #ddd; padding: 16px;">${hospital.name}</td>
                <td style="border: 1px solid #ddd; padding: 16px;">${hospital.type}</td>
                <td style="border: 1px solid #ddd; padding: 16px;">${hospital.mandal}</td>
                <td style="border: 1px solid #ddd; padding: 16px;">${hospital.municipality}</td>
                <td style="border: 1px solid #ddd; padding: 16px;">${hospital.mithra.join("<br>")}</td>
                <td style="border: 1px solid #ddd; padding: 16px;">${hospital.specialities.join("<br>")}</td>
            </tr>`;
        });

        table += "</tbody></table>";

        // Add pagination controls with numbered pages
        let pagination = `<div style="margin-top: 20px; text-align: center;">`;
        
        // Previous Button
        if (currentPage > 1) {
            pagination += `<button onclick="changePage(-1)" style="padding: 10px 15px; margin: 5px; background-color: #007BFF; color: white; border: none; cursor: pointer; border-radius: 5px;">Previous</button>`;
        }

        // Page Numbers
        for (let i = 1; i <= totalPages; i++) {
            pagination += `<button onclick="gotoPage(${i})" style="padding: 10px 15px; margin: 5px; background-color: ${currentPage === i ? '#28A745' : '#ccc'}; color: white; border: none; cursor: pointer; border-radius: 5px;">${i}</button>`;
        }

        // Next Button
        if (currentPage < totalPages) {
            pagination += `<button onclick="changePage(1)" style="padding: 10px 15px; margin: 5px; background-color: #28A745; color: white; border: none; cursor: pointer; border-radius: 5px;">Next</button>`;
        }

        pagination += `</div>`;

        // Display the table and pagination
        hospitalListDiv.innerHTML = table + pagination;
    }
}

// Function to move to next or previous page
function changePage(direction) {
    currentPage += direction;
    displayHospitals();
}

// Function to jump to a specific page
function gotoPage(pageNumber) {
    currentPage = pageNumber;
    displayHospitals();
}
